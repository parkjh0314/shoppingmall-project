package product.model;

public class KindVO {
	private String kindcode;
	private String enkindname;
	private String krkindname;
	
	
	public String getKindcode() {
		return kindcode;
	}
	public void setKindcode(String kindcode) {
		this.kindcode = kindcode;
	}
	public String getEnkindname() {
		return enkindname;
	}
	public void setEnkindname(String enkindname) {
		this.enkindname = enkindname;
	}
	public String getKrkindname() {
		return krkindname;
	}
	public void setKrkindname(String krkindname) {
		this.krkindname = krkindname;
	}
	
	
	
}
